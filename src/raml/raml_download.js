// lib/raml_download.js
// RAML Download Module - Handles downloading RAML project as ZIP file

import archiver from 'archiver';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { parseRamlProjectToFiles, cleanRamlContent, parseRamlProject } from './raml_publishing.js';
import logger from '../documentation/logger.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

/**
 * Generate ZIP file from RAML project content
 */
export async function generateRamlZip(ramlContent, projectName = 'raml-project') {
  logger.info(`[raml_download] Generating ZIP for project: ${projectName}`);
  
  try {
    // Clean RAML content
    const cleanedRamlContent = cleanRamlContent(ramlContent || '');
    
    // Parse RAML project into files
    const files = parseRamlProjectToFiles(cleanedRamlContent);
    
    if (files.length === 0) {
      throw new Error('No valid RAML files found in the content');
    }
    
    logger.info(`[raml_download] Parsed ${files.length} files for ZIP generation`);
    
    // Create temporary directory for ZIP generation
    const tempDir = path.join(__dirname, '..', 'uploads', 'temp');
    if (!fs.existsSync(tempDir)) {
      fs.mkdirSync(tempDir, { recursive: true });
    }
    
    // Generate filename
    const zipFileName = `${projectName}.zip`;
    const zipFilePath = path.join(tempDir, zipFileName);
    
    // Create ZIP archive
    const output = fs.createWriteStream(zipFilePath);
    const archive = archiver('zip', {
      zlib: { level: 9 } // Maximum compression
    });
    
    // Handle archive events
    archive.on('error', (err) => {
      logger.error(`[raml_download] Archive error: ${err.message}`);
      throw err;
    });
    
    // Pipe archive data to the file
    archive.pipe(output);
    
    // Add files to archive with proper directory structure
    files.forEach(file => {
      const filePath = file.path || 'api.raml';
      
      // Normalize path - remove api-project prefix if present
      let normalizedPath = filePath.replace(/^api-project\//, '');
      
      // Ensure we have a proper filename
      if (!normalizedPath || normalizedPath === '') {
        normalizedPath = 'api.raml';
      }
      
      // Add file to archive
      archive.append(file.content || '', { name: normalizedPath });
      logger.debug(`[raml_download] Added file to ZIP: ${normalizedPath}`);
    });
    
    // Add README file with project information
    const readmeContent = generateReadmeContent(projectName, files);
    archive.append(readmeContent, { name: 'README.md' });
    
    // Finalize the archive
    await archive.finalize();
    
    // Wait for the output stream to finish
    await new Promise((resolve, reject) => {
      output.on('close', () => {
        logger.info(`[raml_download] ZIP file created successfully: ${zipFilePath}`);
        resolve();
      });
      output.on('error', reject);
    });
    
    return {
      zipFilePath,
      zipFileName,
      fileCount: files.length,
      size: fs.statSync(zipFilePath).size
    };
    
  } catch (error) {
    logger.error(`[raml_download] Failed to generate ZIP: ${error.message}`);
    throw error;
  }
}

/**
 * Generate README content for the ZIP file
 */
function generateReadmeContent(projectName, files) {
  // Use the categorized project structure for better organization
  const project = parseRamlProject(files.map(f => `>>> ${f.path} <<<\n${f.content}`).join('\n'));
  
  const fileList = files.map(file => {
    const fileName = file.path ? file.path.replace(/^api-project\//, '') : 'api.raml';
    return `- ${fileName}`;
  }).join('\n');
  
  // Generate categorized file list
  let categorizedList = '';
  if (project.mainFile) {
    categorizedList += `- **Main File:** ${project.mainFile.path.replace(/^api-project\//, '')}\n`;
  }
  if (project.traits.length > 0) {
    categorizedList += `- **Traits:** ${project.traits.length} file(s)\n`;
  }
  if (project.securitySchemes.length > 0) {
    categorizedList += `- **Security Schemes:** ${project.securitySchemes.length} file(s)\n`;
  }
  if (project.types.length > 0) {
    categorizedList += `- **Types:** ${project.types.length} file(s)\n`;
  }
  if (project.examples.length > 0) {
    categorizedList += `- **Examples:** ${project.examples.length} file(s)\n`;
  }
  if (project.responses.length > 0) {
    categorizedList += `- **Responses:** ${project.responses.length} file(s)\n`;
  }
  if (project.resourceTypes.length > 0) {
    categorizedList += `- **Resource Types:** ${project.resourceTypes.length} file(s)\n`;
  }
  
  return `# ${projectName}

This ZIP file contains a complete RAML 1.0 API project generated by MuleGenie AI.

## Project Structure

${categorizedList}

## All Files

${fileList}

## Files Description

- **api.raml**: Main API specification file
- **traits/**: Reusable traits for headers, query parameters, etc.
- **securitySchemes/**: Authentication and authorization schemes
- **types/**: Data type definitions
- **examples/**: JSON example files for requests and responses
- **responses/**: Reusable response definitions
- **resourceTypes/**: Reusable resource patterns

## Usage

1. Extract this ZIP file to your local directory
2. Import the project into Anypoint Design Center
3. Review and customize the API specification as needed
4. Deploy your API implementation

## Generated by MuleGenie AI

This RAML project was automatically generated by MuleGenie AI based on your requirements.
Please review and modify the specification as needed for your specific use case.

---
Generated on: ${new Date().toISOString()}
`;
}

/**
 * Clean up temporary ZIP files older than 1 hour
 */
export function cleanupTempZipFiles() {
  try {
    const tempDir = path.join(__dirname, '..', 'uploads', 'temp');
    
    if (!fs.existsSync(tempDir)) {
      return;
    }
    
    const files = fs.readdirSync(tempDir);
    const oneHourAgo = Date.now() - (60 * 60 * 1000);
    
    files.forEach(file => {
      if (file.endsWith('.zip')) {
        const filePath = path.join(tempDir, file);
        const stats = fs.statSync(filePath);
        
        if (stats.mtime.getTime() < oneHourAgo) {
          fs.unlinkSync(filePath);
          logger.debug(`[raml_download] Cleaned up old ZIP file: ${file}`);
        }
      }
    });
  } catch (error) {
    logger.error(`[raml_download] Error cleaning up temp files: ${error.message}`);
  }
}

/**
 * Handle RAML download request
 */
export async function handleRamlDownload(ramlContent, projectName = 'raml-project') {
  logger.info(`[raml_download] Handling RAML download request for project: ${projectName}`);
  
  try {
    // Clean up old temp files first
    cleanupTempZipFiles();
    
    // Generate ZIP file
    const zipInfo = await generateRamlZip(ramlContent, projectName);
    
    return {
      success: true,
      zipFilePath: zipInfo.zipFilePath,
      zipFileName: zipInfo.zipFileName,
      fileCount: zipInfo.fileCount,
      size: zipInfo.size
    };
    
  } catch (error) {
    logger.error(`[raml_download] Download failed: ${error.message}`);
    return {
      success: false,
      error: error.message
    };
  }
}
